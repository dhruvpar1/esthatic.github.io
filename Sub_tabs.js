function f1() {
    var video = document.getElementById('video')
    var image = document.getElementById('image')


    video.style.display = 'block'
    image.style.display = 'none'
    video.play()
    setTimeout(function () {
      video.pause()
      video.currentTime = 0
      video.style.display = 'none'
      image.style.display = 'block'
    }, 5000)
  }
